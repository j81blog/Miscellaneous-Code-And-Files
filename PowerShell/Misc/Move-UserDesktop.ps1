<#
    .SYNOPSIS
        Moves the user's Desktop folder from a legacy 'Windows' subdirectory to the user's home folder root.

    .DESCRIPTION
        This function is designed to correct a folder structure where a user's Desktop is located inside a 'Windows' folder
        within their home directory (e.g., D:\Home\jdoe\Windows\Desktop).
        It moves the Desktop folder to the root of the user's home directory (e.g., D:\Home\jdoe\Desktop).
        If the move is successful, it will then attempt to remove the now-empty 'Windows' folder and a 'Personal Settings' folder.
        The function can operate on a single user or process multiple users from a CSV file.

    .PARAMETER Path
        The root path of the user home folders (e.g., 'D:\Home'). This parameter is mandatory for all operations.

    .PARAMETER User
        The username (and folder name) of the single user to process. Used with the 'SingleUser' parameter set.

    .PARAMETER CsvPath
        The full path to the CSV file containing user information.
        The CSV file must contain a header named 'Username'. Used with the 'CsvInput' parameter set.

    .EXAMPLE
        # Example 1: Process a single user. The script will provide colored status updates.
        Move-UserDesktop -Path 'D:\Home' -User 'jdoe'

    .EXAMPLE
        # Example 2: Process multiple users from a CSV file.
        Move-UserDesktop -Path 'D:\Home' -CsvPath 'C:\temp\users.csv'

    .EXAMPLE
        # Example 3: Use -WhatIf to see what actions would be taken without making any changes.
        Move-UserDesktop -Path 'D:\Home' -User 'jdoe' -WhatIf

    .NOTES
        Version:    1.1
        Author:     John Billekens Consultancy
        CoAuthor:   Gemini (Refactored/Documentation)
        Created:    2025-08-08
#>
function Move-UserDesktop {
    [CmdletBinding(SupportsShouldProcess = $true)]
    param (
        # Parameter Set for processing a single user
        [Parameter( ParameterSetName = 'SingleUser', Mandatory = $true )]
        [string]$User,

        # Parameter Set for processing users from a CSV file
        [Parameter( ParameterSetName = 'CsvInput', Mandatory = $true )]
        [ValidateScript({ Test-Path -Path $_ -PathType Leaf })]
        [string]$CsvPath,

        # Common parameter for both sets
        [Parameter( Mandatory = $true, Position = 0 )]
        [ValidateScript({ Test-Path -Path $_ -PathType Container })]
        [string]$Path,

        # Optional parameter to force the move actions
        [Parameter( Mandatory = $false, Position = 1 )]
        [Switch]$Force
    )

    begin {
        # Nested helper function for colored output that respects the information stream
        function Write-InformationColored {
            [CmdletBinding()]
            param (
                [Parameter(
                    ValueFromPipeline,
                    Mandatory,
                    Position = 0
                )]
                [Alias("Message")]
                [Object]
                $MessageData,

                [ConsoleColor]
                $ForegroundColor = $Host.UI.RawUI.ForegroundColor,

                [ConsoleColor]
                $BackgroundColor = $Host.UI.RawUI.BackgroundColor,

                [Switch]
                $NoNewline
            )
            #Requires -Version 5.0
            $HostMessage = @{
                Message         = $MessageData
                ForegroundColor = $ForegroundColor
                BackgroundColor = $BackgroundColor
                NoNewline       = $NoNewline.IsPresent
            }
            $InfoMessage = [System.Management.Automation.HostInformationMessage]$HostMessage
            Write-Information -MessageData $InfoMessage -InformationAction Continue
        }

        # Set preference to ensure Write-Information (and our helper) displays output
        $InformationPreference = 'Continue'
        $UserList = @()

        try {
            if ($PSCmdlet.ParameterSetName -eq 'CsvInput') {
                Write-Verbose "Processing users from CSV file: $($CsvPath)"
                $Users = Import-Csv -Path $CsvPath
                if ($null -ne $Users) {
                    if ('Username' -in $Users[0].PSObject.Properties.Name) {
                        $UserList = $Users.Username
                    } else {
                        throw "CSV file '$($CsvPath)' does not contain a 'Username' column."
                    }
                }
            } else {
                Write-Verbose "Processing single user: $($User)"
                $UserList = @($User)
            }
        } catch {
            Write-Error -Message "Failed to initialize user list. Error: $($_.Exception.Message)"
            return # Stop processing if the input is invalid
        }
    }

    process {
        foreach ($CurrentUser in $UserList) {
            $UserHomeFolder = Join-Path -Path $Path -ChildPath $CurrentUser
            Write-InformationColored -MessageData "Processing user '$($CurrentUser)' in path '$($UserHomeFolder)'" -ForegroundColor Cyan

            if (-not (Test-Path -Path $UserHomeFolder)) {
                Write-Warning "User home folder not found for '$($CurrentUser)' at path '$($UserHomeFolder)'. Skipping."
                continue
            }

            $SourceDesktopPath = Join-Path -Path $UserHomeFolder -ChildPath 'Windows\Desktop'
            $DestinationDesktopPath = Join-Path -Path $UserHomeFolder -ChildPath 'Desktop'
            $WindowsFolderPath = Join-Path -Path $UserHomeFolder -ChildPath 'Windows'
            $PersonalSettingsPath = Join-Path -Path $UserHomeFolder -ChildPath 'Personal Settings'

            if (-not (Test-Path -Path $SourceDesktopPath)) {
                Write-Warning "Source folder '$($SourceDesktopPath)' does not exist for user '$($CurrentUser)'`r`n`t(Or it may have been moved). Skipping."
                continue
            }

            if ((Test-Path -Path $DestinationDesktopPath) -and -not $Force) {
                Write-Warning "Destination folder '$($DestinationDesktopPath)' already exists for user '$($CurrentUser)'.`r`n`tSkipping to avoid data loss."
                continue
            } elseif ($Force) {
                Write-InformationColored -MessageData "Force option is enabled. Proceeding with the move operation for user '$($CurrentUser)'." -ForegroundColor Yellow
            }

            # --- Step 1: Move the Desktop folder ---
            $MoveSuccessful = $false
            try {
                if ($PSCmdlet.ShouldProcess($SourceDesktopPath, "Move to '$($DestinationDesktopPath)'")) {
                    Move-Item -Path $SourceDesktopPath -Destination $DestinationDesktopPath -ErrorAction Stop -Force:$Force
                    Write-InformationColored -MessageData "=> Successfully moved Desktop folder for user '$($CurrentUser)'." -ForegroundColor Green
                    $MoveSuccessful = $true
                }
            } catch {
                Write-Warning "Failed to move Desktop folder for user '$($CurrentUser)'. Error: $($_.Exception.Message)"
            }

            # --- Step 2 & 3: Delete folders only if the move was successful ---
            if ($MoveSuccessful) {
                # Delete the Windows folder
                if (Test-Path -Path $WindowsFolderPath) {
                    try {
                        if ($PSCmdlet.ShouldProcess($WindowsFolderPath, "Remove Folder")) {
                            Remove-Item -Path $WindowsFolderPath -Recurse -Force -ErrorAction Stop
                            Write-InformationColored -MessageData "=> Successfully removed Windows folder for user '$($CurrentUser)'." -ForegroundColor Green
                        }
                    } catch {
                        Write-Warning "Could not remove folder '$($WindowsFolderPath)'. Error: $($_.Exception.Message)"
                    }
                }

                # Delete the Personal Settings folder
                if (Test-Path -Path $PersonalSettingsPath) {
                    try {
                        if ($PSCmdlet.ShouldProcess($PersonalSettingsPath, "Remove Folder")) {
                            Remove-Item -Path $PersonalSettingsPath -Recurse -Force -ErrorAction Stop
                            Write-InformationColored -MessageData "=> Successfully removed Personal Settings folder for user '$($CurrentUser)'." -ForegroundColor Green
                        }
                    } catch {
                        Write-Warning "Could not remove folder '$($PersonalSettingsPath)'. Error: $($_.Exception.Message)"
                    }
                }
            }
            Write-InformationColored -MessageData "Finished processing user '$($CurrentUser)'." -ForegroundColor Cyan
            Write-InformationColored -MessageData "" # Add a blank line for readability between users
        }
    }
}

